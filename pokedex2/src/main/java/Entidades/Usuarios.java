package Entidades;

/**
 *
 * @author Propietario
 */
public class Usuarios {
    private int id;
    private String nombre;
    
    public Usuarios(int id, String nombre){
        this.id = id;
        this.nombre=nombre;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
}
