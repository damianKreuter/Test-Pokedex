    public void pedirUser() throws SQLException{
        JComboBox<Usuarios> comboUsuarios = new JComboBox<Usuarios>();
        Select select = new Select();
        ResultSet res = select.todosLosUser();
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        while(res.next()){
            comboUsuarios.addItem(new Usuarios(res.getInt(1), res.getString(2)));
            model.addElement(res.getString(2));
        }
        
        jComboBox1.setModel(model);
        select.cerrarConsulta();
    }
    
    public void reactiar() throws SQLException{
        pedirUser();
    }
    


